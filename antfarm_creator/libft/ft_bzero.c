#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t		i;
	char		*str;

	i = 0;
	str = s;
	if (n > 0)
	{
		while (n > 0)
		{
			str[i] = 0;
			i++;
			n--;
		}
	}
}
