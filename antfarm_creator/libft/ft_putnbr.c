#include "libft.h"

void	ft_putnbr(int n)
{
	char			c;
	unsigned int	num;

	num = n;
	if (n < 0)
	{
		num = n * (-1);
		write(1, "-", 1);
	}
	if (num > 9)
		ft_putnbr((num / 10));
	c = (num % 10) + 48;
	write(1, &c, 1);
}
