char	*ft_strchr(const char *s, int c)
{
	char	t;
	int		len;
	int		i;

	i = 0;
	len = 0;
	t = c;
	while (s[len] != '\0')
		len++;
	while (i <= len)
	{
		if (s[i] == t)
			return ((char*)&s[i]);
		i++;
	}
	return (0);
}
