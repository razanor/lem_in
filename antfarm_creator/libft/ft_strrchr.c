char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	t;

	i = 0;
	t = c;
	while (s[i] != '\0')
		i++;
	while (i >= 0)
	{
		if (s[i] == t)
			return ((char*)&s[i]);
		i--;
	}
	return (0);
}
