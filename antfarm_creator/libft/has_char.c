int	has_char(char *line, char c)
{
	int i;
	int num_of_c;

	i = -1;
	num_of_c = 0;
	while (line[++i])
	{
		if (line[i] == c)
			num_of_c++;
	}
	if (num_of_c == 1)
		return (1);
	return (0);
}
